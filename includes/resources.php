    <h2 class="text-center">Resources</h2>

    <p>I am a new conlanger. I have found these resources to be very helpful on helping me create my first language. If you have any suggestions/recommendations for this list, feel free to let me know.</p>

    <ul>
    	<li><strong><a href="http://library.conlang.org/education/NathanRichardsonWorksheets.pdf">Nathan Richardson's Conlang Worksheets</a></strong></br>
            Worksheets to aid in creating a language of your own.</li>
        <li><strong><a href="http://folk.uib.no/hnohf/qcourse.htm">Quenya Course</a></strong></br>
            Free online course that teaches you how to speak Quenya, the Elven language of Lord of the Rings. it's not only useful simply to learn this language, but mostly because it teaches you the specifics of language creation and what goes into such an endeavor - and teaches you as if you have no clue whatsoever as to what's involved. Which I don't.</li>
        <li><strong><a href="http://www.zompist.com/kit.html">The Language Construction Kit</a></strong></br>
            The famous construction kit, which does what it says: helps you construct a language.</li>
        <li><strong><a href="http://www.sicher.org/2011/10/18/how-to-create-a-language-in-one-day/">How to Create a Language in One Day</a></strong></br>
            Interesting method on how a writer/game creator created a language from scratch, without really knowing what he was doing.</li>
        <li><strong><a href="http://dedalvs.com/index.html">David J. Peterson's Blog</a></strong></br>
            Informational site by the man who created othraki for the HBO series <em>Game of Thrones</em>. Lots of info and resources here.</li>
        <li><strong><a href="https://www.reddit.com/r/conlangs/comments/1dgiqh/recolangmo_session_1_introduction_to_your_language/">Reddit's ReCoLangMo</a></strong></br>
            Reddit's resource/home page for conlanging.</li>
        <li><strong><a href="http://phrontistery.info/">The Phrontistery</a></strong></br>
            A dictionary of weird and unusual words.</li>
        <li><strong><a href="http://dictionary.com">Dictionary</a></strong></br>
            Online dictionary.</li>
        <li><strong><a href="http://www.thesaurus.com/">Thesaurus</a></strong></br>
            Online Thesaurus.</li>
    </ul>

